        <div class="arabic-quote">
            <h3>Keutamaan Menyantuni Anak Yatim</h3>
            <p class="arabic-text">
                عَنْ سَهْلِ بْنِ سَعْدٍ رضي الله عنه قَالَ : قَالَ رَسُولُ اللَّهِ صلى الله عليه وسلم : أَنَا
                وَكَافِلُ الْيَتِيمِ فِى الْجَنَّةِ هكَذَا، وَأَشَارَ بِالسَّبَّابَةِ وَالْوُسطَى وَفَرَّجَ
                بَيْنَهُمَا شَيْئاً
            </p>
            <p class="source">[HR al-Bukhari no. 4998 dan 5659]</p>
        </div>
        <div class="translation">
            <p>"Aku dan orang yang menanggung anak yatim (kedudukannya) di surga seperti ini" - kemudian Rasulullah ﷺ mengisyaratkan jari telunjuk dan jari tengah beliau, serta agak merenggangkan keduanya.</p>
        </div>
